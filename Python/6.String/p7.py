# 7. Comparing strings

# Equality (==) and Inequality (!=):
str1 = "hello"
str2 = "Hello"
print(str1 == str2)  # Output: False
print(str1 != str2)  # Output: True

# Lexicographical Comparison (<, >, <=, >=):
str1 = "apple"
str2 = "banana"
print(str1 < str2)   # Output: True
print(str1 > str2)   # Output: False
print(str1 <= str2)  # Output: True
print(str1 >= str2)  # Output: False

# Using (in) Operator
text = "Hello, World!"
print("Hello" in text)  # Output: True
print("Python" in text) # Output: False


