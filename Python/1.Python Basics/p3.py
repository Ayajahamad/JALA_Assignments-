# 3.  Define variables for different Data Types int, Boolean, char, float, double and print on the Console.

my_int = 42
my_bool = True
my_char = 'A'
my_float = 3.14
my_double = 3.14159

print(f"Type of {my_int} : {type(my_int)}")
print(f"Type of {my_bool} : {type(my_bool)}")
print(f"Type of {my_char} : {type(my_char)}")
print(f"Type of {my_float} : {type(my_float)}")
print(f"Type of {my_double} : {type(my_double)}")

"""
    Integer (int): my_int is assigned the value 42.
    Boolean (bool): my_bool is assigned the value True.
    Character (str): my_char is assigned the character 'A'.
    Float (float): my_float is assigned the value 3.14.
    Double (float): my_double is assigned the value 3.14159.
"""